    private float vendido(){
        float vend;
        vend=Float.parseFloat(Venta.getText().trim());
        return vend;
    }
    private float comision(){
        float comi;
        comi=(float) (0.05*vendido());
        return comi;
    }private float descuento(){
        float desc;
        desc=(float) (0.15*Sueldo_bruto());
        return desc;
    }private float Sueldo_bruto(){
        float sbrr;
        sbrr=(float) (350.75+comision());
        return sbrr;
    }private float Sueldo_Neto(){
        float snet;
        snet=Sueldo_bruto()-descuento();
        return snet;
    }
private void mostrar(){
        Comi.setText(String.valueOf(comision()));
        Desc.setText(String.valueOf(descuento()));
        Sbruto.setText(String.valueOf(Sueldo_bruto()));
        Sneto.setText(String.valueOf(Sueldo_Neto()));

    }
   
